package U0;

public class DivisibleForDivisibleWhile {
    public static void main(String[] args) {
        for (int i = 0; i <=100 ; i++) {
            if (i%2==0){
                System.out.println(i);
            }
            else if (i%3==0){
                System.out.println(i);
            }
        }
    }
}
